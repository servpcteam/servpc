<p><strong>REGULAMIN SKLEPU INTERNETOWEGO [nazwa]</strong></p>
<p><strong>z [data pierwszej publikacji na stronie internetowej]</strong></p>
<p></p>
<p><strong>I. Definicje</strong></p>
<p>Użyte w Regulaminie pojęcia oznaczają:</p>
<p>1. <strong>Klient</strong> – osoba fizyczna, osoba prawna lub jednostka organizacyjna nie będącą osobą prawną, której
    przepisy szczególne przyznają zdolność prawną, która dokonuje Zamówienia w ramach Sklepu;</p>
<p>2. <strong>Kodeks Cywilny</strong> – ustawa z dnia 23 kwietnia 1964 r. (Dz. U. Nr 16, poz. 93 ze zm.); </p>
<p>3. <strong>Regulamin</strong> – niniejszy Regulamin świadczenia usług drogą elektroniczną w ramach sklepu
    internetowego [nazwa sklepu]; </p>
<p>4. <strong>Sklep internetowy (Sklep)</strong> – serwis internetowy dostępny pod [adresem], za pośrednictwem którego
    Klient może w szczególności składać Zamówienia; </p>
<p>5. <strong>Towar</strong> – produkty prezentowane w Sklepie Internetowym;</p>
<p>6. <strong>Umowa sprzedaży</strong> – umowa sprzedaży Towarów w rozumieniu Kodeksu Cywilnego, zawarta pomiędzy
    [podmiotem prowadzącym sklep internetowy] a Klientem, zawierana z wykorzystaniem serwisu internetowego Sklepu;</p>
<p>7. <strong>Ustawa o szczególnych warunkach sprzedaży konsumenckiej</strong> – ustawa z dnia 27 lipca 2002 r. o
    szczególnych warunkach sprzedaży konsumenckiej oraz o zmianie Kodeksu Cywilnego (Dz. U. Nr 141, poz. 1176 ze zm.);
</p>
<p>8. <strong>Ustawa o świadczeniu usług drogą elektroniczną</strong> – ustawa z dnia 18 lipca 2002 r. o świadczeniu
    usług drogą elektroniczną (Dz. U. Nr 144, poz. 1204 ze zm.);</p>
<p>9. <strong>Zamówienie</strong> – oświadczenie woli Klienta, zmierzające bezpośrednio do zawarcia Umowy sprzedaży,
    określające w szczególności rodzaj i liczbę Towaru.</p>
<p></p>
<p><strong>II. Postanowienia ogólne</strong></p>
<p>2.1. Niniejszy Regulamin określa zasady korzystania ze sklepu internetowego dostępnego pod [adresem]. </p>
<p>2.2. Niniejszy Regulamin jest regulaminem, o którym mowa w art. 8 Ustawy o świadczeniu usług drogą
    elektroniczną. </p>
<p>2.3. Sklep internetowy, działający pod [adresem], prowadzony jest przez [oznaczenie zgodne z częścią I.
    Podręcznika]. </p>
<p>
<span>2.4.
Niniejszy Regulamin określa w szczególności </span><span>[w
